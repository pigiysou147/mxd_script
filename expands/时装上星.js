//获取装备类型
function getItemType(itemid) {
	var type = Math.floor(itemid / 10000);
	switch(type) {
		case 100:
			return 0; //帽子
		case 104:
			return 1; //上衣
		case 105:
			return 2; //套装
		case 106:
			return 3; //裤裙
		case 107:
			return 4; //鞋子
		case 108:
			return 5; //手套
		default:
			if(type == 120)
				return -1;
			if(type == 135)
				return -1;
			var type = Math.floor(type / 10);
			if(type == 12 || type == 13 || type == 14 || type == 15 || type == 17) {
				return 7; //武器
			}
			return -1;
	}
}
